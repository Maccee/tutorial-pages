// Function to calculate the area of a triangle
function triangleArea(p1, p2, p3) {
  return (
    0.5 *
    Math.abs(
      p1[0] * (p2[1] - p3[1]) +
        p2[0] * (p3[1] - p1[1]) +
        p3[0] * (p1[1] - p2[1])
    )
  );
}

// Initialize variables for the total area and the weighted sum of centroids
let totalArea = 0;
let weightedSum = [0, 0];

// Loop through the coordinates to calculate the centroids of triangles and their areas
for (let i = 0; i < coordinates[0].length - 1; i++) {
  const triangle = [
    coordinates[0][i],
    coordinates[0][i + 1],
    coordinates[0][coordinates[0].length - 1],
  ];
  const area = triangleArea(triangle[0], triangle[1], triangle[2]);
  const centroid = [
    (triangle[0][0] + triangle[1][0] + triangle[2][0]) / 3.0,
    (triangle[0][1] + triangle[1][1] + triangle[2][1]) / 3.0,
  ];
  weightedSum[0] += centroid[0] * area;
  weightedSum[1] += centroid[1] * area;
  totalArea += area;
}

// Calculate the geological center by dividing the weighted sum by the total area
const geologicalCenter = [
  weightedSum[0] / totalArea,
  weightedSum[1] / totalArea,
];

